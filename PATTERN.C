#include<stdio.h>
#include<conio.h>
void main()
{
int n,i,j,k,l;
clrscr();
printf("Enter the no. of rows=");
scanf("%d",&n);
for(i=1;i<=n;i++)
{
for(j=1;j<=n-i;j++)
{
printf(" ");
}
for(k=1;k<=i;k++)
{
printf("*");
}
for(l=1;l<=i-1;l++)
{
printf("*");
}
printf("\n");
}
for(i=1;i<=n-1;i++)
{
for(j=1;j<=i;j++)
{
printf(" ");
}
for(k=1;k<=(n-1)+1-i;k++)
{
printf("*");
}
for(l=1;l<=(n-1)-i;l++)
{
printf("*");
}
printf("\n");
}
getch();
}
